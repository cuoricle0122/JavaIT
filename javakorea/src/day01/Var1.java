package day01;

public class Var1 {
	public static void main(String[] args) {
		// 프로그래밍 언어 : 개발자가 무언가를 코딩해서 컴퓨터가 결과를 출력하게 하는 것
		// 변수 : 저장공간 (무언가 값을 저장해놓았다가 키워드로 사용)
		String 이름 = "홍길동";         // '홍길동'이라는 값을 '이름'이라는 공간에 저장
		
		System.out.println(이름+"1");
		System.out.println(이름+"2");
		System.out.println(이름+"3");
		System.out.println(이름+"4");
		System.out.println(이름+"5");
		System.out.println(이름+"6");
		System.out.println(이름+"7");
		System.out.println(이름+"8");
		System.out.println(이름+"9");
		System.out.println(이름+"10");
		
		
		// 홍길동x -> 고길동o
		
		// 변수 : 값을 저장하는 공간 (재활용)
	}
}
